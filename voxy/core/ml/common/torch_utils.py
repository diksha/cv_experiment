import random

import numpy as np
import torch


def initialize_random_state(seed: int = 1234):
    """
    Initializes all the random state generators for reproducible training

    Args:
        seed (int, optional): seed to choose. Defaults to 1234.
    """
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    torch.cuda.manual_seed(seed)
    torch.backends.cudnn.deterministic = True
